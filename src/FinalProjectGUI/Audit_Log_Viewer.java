    private void loadAuditLogs() {
        DefaultTableModel model = (DefaultTableModel) auditLogsTable.getModel();
        model.setRowCount(0); // Clear existing rows

        // Populate the table with audit log entries
        for (AuditLog log : auditLogs) {
            model.addRow(new Object[]{log.getLogId(), log.getUser(), log.getAction(), log.getDate()});
        }
    }

    private AuditLog findLogById(String logId) {
        for (AuditLog log : auditLogs) {
            if (log.getLogId().equals(logId)) {
                return log;
            }
        }
        return null;
    }

