    private void viewDetailsButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewDetailsButtonActionPerformed
        int selectedRow = auditLogsTable.getSelectedRow();
        if (selectedRow != -1) {
            String logId = (String) auditLogsTable.getValueAt(selectedRow, 0);
            AuditLog log = findLogById(logId);
            if (log != null) {
                JOptionPane.showMessageDialog(this, log.getLogDetails(), "Log Details", JOptionPane.INFORMATION_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Please select a log to view details.");
        }

    }//GEN-LAST:event_viewDetailsButtonActionPerformed

