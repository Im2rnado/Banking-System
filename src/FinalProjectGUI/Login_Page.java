    private void navigateToDashboard(User user) {
        switch (user.getRole().toLowerCase()) {
            case "customer":
                Customer customer = dbManager.returnCustomerFromUser(user);
                Customer_Dashboard customerDashboard = new Customer_Dashboard(customer);
                customerDashboard.setVisible(true);
                break;
            case "manager":
                Manager manager = dbManager.returnManagerFromUser(user);
                Manager_Dashboard managerDashboard = new Manager_Dashboard(manager);
                managerDashboard.setVisible(true);
                break;
            case "staff":
                Staff staff = dbManager.returnStaffFromUser(user);
                Staff_Dashboard staffDashboard = new Staff_Dashboard(staff);
                staffDashboard.setVisible(true);
                break;
            case "admin":
                Admin admin = dbManager.returnAdminFromUser(user);
                Admin_Dashboard adminDashboard = new Admin_Dashboard(admin);
                adminDashboard.setVisible(true);
                break;
            default:
                JOptionPane.showMessageDialog(this, "Unknown user role.", "Error", JOptionPane.ERROR_MESSAGE);
                return;
        }
        dispose();
    }

